namespace ExportIfc.Logging;

/// <summary>
/// Общая точка синхронизации для операций форматированного вывода.
/// </summary>
/// <remarks>
/// Назначение:
/// 1. Координирует доступ к общему консольному выводу.
/// 2. Не допускает перемешивания строк и панелей при одновременной записи из разных потоков.
///
/// Контракты:
/// 1. Все операции записи через Spectre.Console должны выполняться под этой синхронизацией.
/// 2. Класс не содержит логики форматирования и не хранит состояние прогона.
/// </remarks>
internal static class ConsoleSync
{
    /// <summary>
    /// Объект блокировки для всех операций записи в консоль.
    /// </summary>
    public static readonly object Sync = new();
}