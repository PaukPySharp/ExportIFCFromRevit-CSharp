using ExportIfc.Config;

namespace ExportIfc.Transfer;

/// <summary>
/// Контейнер transport-пакета для передачи заданий между оркестратором и Revit-процессом.
/// </summary>
/// <remarks>
/// Назначение:
/// 1. Объединяет общие параметры batch-пакета и список моделей текущего запуска.
/// 2. Сериализуется в <see cref="ProjectFileNames.TmpJson"/> как единая точка передачи данных в add-in.
/// 3. Служит снимком состава пакета на момент его формирования.
///
/// Контракты:
/// 1. Один экземпляр описывает пакет только для одной major-версии Revit.
/// 2. <see cref="CreatedUtc"/> фиксирует момент формирования пакета в UTC.
/// 3. <see cref="Items"/> хранит порядок моделей таким, каким он был сформирован оркестратором.
/// 4. Контейнер не интерпретирует бизнес-смысл отдельных полей моделей,
///    а только переносит их между процессами.
/// </remarks>
public sealed class TransferEnvelope
{
    /// <summary>
    /// Идентификатор запуска оркестратора, сформировавшего пакет.
    /// </summary>
    /// <remarks>
    /// Используется как часть межпроцессного контракта,
    /// чтобы add-in мог отличить свой пакет от чужого при пересечении запусков.
    /// </remarks>
    public string RunId { get; init; } = string.Empty;

    /// <summary>
    /// Старшая версия Revit, для которой сформирован пакет.
    /// </summary>
    /// <remarks>
    /// Все элементы внутри пакета должны выполняться одним Revit-процессом
    /// этой major-версии.
    /// </remarks>
    public int RevitMajor { get; init; }

    /// <summary>
    /// Момент формирования пакета в UTC.
    /// </summary>
    /// <remarks>
    /// Это техническая метка формирования transport-пакета.
    /// Она не участвует в сверке состава и порядка моделей.
    /// </remarks>
    public DateTime CreatedUtc { get; init; }

    /// <summary>
    /// Набор элементов, подлежащих экспорту.
    /// </summary>
    /// <remarks>
    /// Порядок элементов сохраняется при сериализации
    /// и далее используется при сверке с <see cref="ProjectFileNames.TaskFileDisplayName"/> пакета.
    /// </remarks>
    public List<TransferItem> Items { get; init; } = new();
}
