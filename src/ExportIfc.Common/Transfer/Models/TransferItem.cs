namespace ExportIfc.Transfer;

/// <summary>
/// Параметры экспорта одной модели, передаваемые оркестратором в add-in.
/// </summary>
/// <remarks>
/// Назначение:
/// 1. Описывает одну RVT-модель внутри transport-пакета.
/// 2. Хранит параметры двух направлений IFC-экспорта:
///    с маппингом и без маппинга.
/// 3. Служит чистым DTO между оркестратором и add-in.
///
/// Контракты:
/// 1. Экземпляр всегда относится к одной конкретной RVT-модели.
/// 2. Активность направления экспорта определяется не наличием всех полей,
///    а значением каталогов выгрузки.
/// 3. Отключённое направление экспорта кодируется отсутствием каталога выгрузки
///    для соответствующего маршрута.
/// 4. Конфигурационные поля модели могут оставаться заполненными,
///    даже если конкретный маршрут в текущем пакете отключён.
/// 5. JSON-конфигурации и файл сопоставления категорий Revit и классов IFC передаются
///    как часть общей конфигурации модели и не определяют сами по себе,
///    нужно ли исполнять конкретный маршрут.
/// </remarks>
public sealed class TransferItem
{
    /// <summary>
    /// Полный путь к RVT-модели.
    /// </summary>
    public required string ModelPath { get; init; }

    /// <summary>
    /// Каталог выгрузки IFC с маппингом.
    /// </summary>
    /// <remarks>
    /// <see langword="null"/> означает, что маршрут с маппингом выключен
    /// для текущей модели.
    /// </remarks>
    public string? OutputDirMapping { get; init; }

    /// <summary>
    /// Путь к JSON-конфигурации экспорта с маппингом.
    /// </summary>
    public required string MappingJson { get; init; }

    /// <summary>
    /// Путь к txt-файлу сопоставления категорий Revit и классов IFC.
    /// </summary>
    public required string IfcClassMappingFile { get; init; }

    /// <summary>
    /// Каталог выгрузки IFC без маппинга.
    /// </summary>
    /// <remarks>
    /// <see langword="null"/> означает, что маршрут без маппинга выключен.
    /// </remarks>
    public string? OutputDirNoMap { get; init; }

    /// <summary>
    /// Путь к JSON-конфигурации экспорта без маппинга.
    /// </summary>
    /// <remarks>
    /// Значение относится к конфигурации модели и может оставаться заполненным,
    /// даже если маршрут без маппинга в текущем пакете не активен.
    /// Факт выполнения маршрута определяется не этим свойством само по себе,
    /// а рабочим набором условий batch-экспорта.
    /// </remarks>
    public string? NoMapJson { get; init; }
}
