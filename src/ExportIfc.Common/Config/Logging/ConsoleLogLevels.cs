namespace ExportIfc.Config;

/// <summary>
/// Коды уровней сообщений для консольного лога оркестратора.
/// </summary>
/// <remarks>
/// Назначение:
/// 1. Централизует короткие текстовые маркеры уровней для консольного вывода.
/// 2. Убирает "магические строки" из логгера и связанных компонентов.
/// 3. Даёт единое место расширения, если позже появятся новые уровни.
///
/// Контракты:
/// 1. Значения являются частью визуального формата консольного лога.
/// 2. Коды хранятся в сокращённом виде.
/// 3. Это не пользовательские настройки и не должны выноситься во внешний ini/json.
/// </remarks>
public static class ConsoleLogLevels
{
    /// <summary>Информационное сообщение.</summary>
    public const string Info = "INF";

    /// <summary>Предупреждение.</summary>
    public const string Warning = "WRN";

    /// <summary>Ошибка.</summary>
    public const string Error = "ERR";
}
