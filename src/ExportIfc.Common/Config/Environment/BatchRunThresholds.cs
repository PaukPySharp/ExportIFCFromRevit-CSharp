namespace ExportIfc.Config;

/// <summary>
/// Числовые пороги batch-запуска add-in и оркестратора.
/// </summary>
/// <remarks>
/// Назначение:
/// 1. Централизует небольшие runtime-пороги batch-сценария.
/// 2. Убирает дублирование одинаковых чисел между add-in и оркестратором.
/// 3. Делает пороги batch-контракта отдельно находимыми внутри config-слоя.
///
/// Контракты:
/// 1. Значения используются как служебные runtime-пороги.
/// 2. Эти пороги влияют на поведение batch-жизненного цикла, но не меняют формат данных.
/// 3. Изменение значений влияет на ожидание и завершение Revit-процесса.
/// </remarks>
public static class BatchRunThresholds
{
    /// <summary>
    /// Окно штатного закрытия Revit после записи итогового статуса add-in.
    /// </summary>
    public const int GracefulCloseWindowSeconds = 30;
}
