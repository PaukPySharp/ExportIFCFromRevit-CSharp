namespace ExportIfc.Config;

/// <summary>
/// Короткие статусы пакетного выполнения add-in.
/// </summary>
/// <remarks>
/// Назначение:
/// 1. Централизует строковые статусы итогового результата batch-запуска.
/// 2. Убирает литералы "OK", "PARTIAL" и "FAILED" из кода add-in и оркестратора.
/// 3. Делает статусный контракт отдельно находимым внутри конфиг-слоя.
///
/// Контракты:
/// 1. Значения используются как короткие служебные коды итогового статуса.
/// 2. Эти коды являются частью контрактов обмена между компонентами.
/// 3. Здесь хранятся только итоговые статусы запуска, без текстовых пояснений и диагностических сообщений.
/// </remarks>
public static class BatchRunStatuses
{
    /// <summary>
    /// Пакет выполнен без ошибок.
    /// </summary>
    public const string Ok = "OK";

    /// <summary>
    /// Пакет выполнен частично: часть моделей завершилась ошибкой.
    /// </summary>
    public const string Partial = "PARTIAL";

    /// <summary>
    /// Пакет завершился ошибкой.
    /// </summary>
    public const string Failed = "FAILED";
}
