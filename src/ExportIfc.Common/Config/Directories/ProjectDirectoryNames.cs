namespace ExportIfc.Config;

/// <summary>
/// Имена ключевых каталогов проекта.
/// </summary>
/// <remarks>
/// Назначение:
/// 1. Централизует базовые имена каталогов, используемых в структуре проекта.
/// 2. Разводит имена директорий и имена файлов по разным модулям.
/// 3. Делает логику построения путей более предсказуемой.
///
/// Контракты:
/// 1. Здесь хранятся только имена каталогов, без абсолютных путей.
/// 2. Значения используются при построении путей в <see cref="ProjectPaths"/> и <see cref="ProjectFiles"/>.
/// 3. Изменение этих имён влияет на физическую структуру проектных каталогов.
/// </remarks>
public static class ProjectDirectoryNames
{
    /// <summary>
    /// Имя каталога с административными файлами внешнего оркестратора.
    /// </summary>
    public const string AdminData = "admin_data";

    /// <summary>
    /// Имя каталога с рабочей историей состояний моделей.
    /// </summary>
    public const string History = "history";

    /// <summary>
    /// Имя каталога с конфигурационными и служебными файлами проекта.
    /// </summary>
    public const string Settings = "_settings";
}